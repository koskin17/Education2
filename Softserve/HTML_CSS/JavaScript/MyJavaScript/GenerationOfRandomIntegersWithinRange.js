/*
Генерація випадкових цілих чисел в межах діапазону
Ви можете згенерувати випадкове ціле число в діапазоні від нуля до заданого числа. Ви також можете вибрати будь-яке інше менше число для цього діапазону.

Мінімальне число називають min, а максимальне — max.

Завдяки цій формулі можна отримати випадкове ціле число від min до max. Зробіть паузу, щоб прочитати і зрозуміти, що робить цей код:

Math.floor(Math.random() * (max - min + 1)) + min
Створіть функцію під назвою randomRange, яка приймає діапазон myMin та myMax і повертає випадкове ціле число, яке більше або дорівнює myMin і менше або дорівнює myMax.
*/

function randomRange(myMin, myMax) {
  return Math.floor(Math.random() * (myMax - myMin + 1)) + myMin;
}

/*
Як це працює:
Math.random() генерує число від 0 (включно) до 1 (не включно).
Множимо це число на (myMax - myMin + 1), щоб отримати значення в діапазоні від 0 до (myMax - myMin).
Використовуємо Math.floor() для округлення до найближчого меншого цілого числа.
Додаємо myMin, щоб змістити результат в потрібний діапазон від myMin до myMax включно.
Приклад використання:
javascript
Копировать код
console.log(randomRange(5, 10)); // може повернути число від 5 до 10 включно
Ця функція завжди повертає ціле число в межах від myMin до myMax.
*/