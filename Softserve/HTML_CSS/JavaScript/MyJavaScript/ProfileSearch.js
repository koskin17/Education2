/*
Пошук профілю
У нас є масив об’єктів, які представляють різних людей зі списку контактів.

Функція lookUpProfile, яка приймає name та властивість (prop) як аргументи, вже записана для вас.

Функція повинна перевірити, чи name дійсно є ім’ям контакту (firstName) і надана властивість (prop) є властивістю цього контакту.

Якщо обидва є істинними, то поверніть значення цієї власності.

Якщо name не відповідає жодному контакту, поверніть рядок No such contact.

Якщо prop не збігається з жодною дійсною властивістю контакту, що відповідає name, поверніть рядок No such property.
*/
function lookUpProfile(name, prop) {
  // Проходимо через кожен контакт у масиві
  for (let i = 0; i < contacts.length; i++) {
    // Перевіряємо, чи збігається ім'я контакту
    if (contacts[i].firstName === name) {
      // Якщо властивість існує, повертаємо її значення
      if (contacts[i].hasOwnProperty(prop)) {
        return contacts[i][prop];
      } else {
        // Якщо властивість не знайдена, повертаємо рядок "No such property"
        return "No such property";
      }
    }
  }
  // Якщо ім'я контакту не знайдено, повертаємо рядок "No such contact"
  return "No such contact";
}
/*
Пояснення:
Перевірка імені: Функція проходить через всі контакти в масиві contacts за допомогою циклу for. Якщо firstName контакту збігається з переданим параметром name, вона продовжує перевірку.
Перевірка властивості: Якщо ім'я збігається, функція перевіряє, чи є в об'єкті така властивість, використовуючи метод hasOwnProperty(prop). Якщо властивість є, функція повертає її значення.
Якщо властивості немає: Якщо властивість не знайдена, повертається рядок "No such property".
Якщо контакт не знайдено: Якщо цикл закінчився і контакт з таким ім'ям не знайдений, функція повертає рядок "No such contact".
Приклад виклику:
javascript
Копировать код
console.log(lookUpProfile("Akira", "likes")); // Поверне ["Pizza", "Coding", "Brownie Points"]
console.log(lookUpProfile("Sherlock", "number")); // Поверне "0487345643"
console.log(lookUpProfile("Harry", "address")); // Поверне "No such property"
console.log(lookUpProfile("John", "number")); // Поверне "No such contact"
Ця функція дозволяє шукати значення за ім'ям контакту і властивістю, виконуючи всі необхідні перевірки.
*/
